public class ControlFlowExercises {
    public static void main(String[] args){


        int i = 5;
        while (i < 16) {
            System.out.println(i);
            i++;
        }

        int doWhile = 0;
        do{
            System.out.println(doWhile);
            doWhile +=2 ;
        } while (doWhile <= 100);

    }
}
